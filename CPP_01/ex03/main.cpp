#include "Weapon.hpp"
#include "HumanA.hpp"
#include "HumanB.hpp"
#include <string>
#include <iostream>

int main()
{
	Weapon club = Weapon("crude spiked club");

	HumanA bob("Bob", club);
	bob.attack();
	club.setType("some other type of club");
	bob.attack();
	
	Weapon hammer = Weapon("crude spiked club");

	HumanB jim("Jim");
	jim.setWeapon(hammer);
	jim.attack();
	hammer.setType("some other type of club");
	jim.attack();
	
}